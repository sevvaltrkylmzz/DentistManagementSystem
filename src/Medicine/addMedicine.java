    private void addDctorbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addDctorbtnMouseClicked
        connection = Connector.ConnectDb();
        if (connection != null) {
            String id = txtmid.getText();
            String fname = txtmtype.getText();
            String mname = txtmname.getText();
            String lname = txtmtid.getText();
            
            String sql = "insert into medicine(MID,MType,MName,TID) value (?,?,?,?)";
            try {
                prp = connection.prepareStatement(sql);
                prp.setString(1, id);
                prp.setString(2, fname);
                prp.setString(3, mname);
                prp.setString(4, lname);
               
               
                prp.execute();
                JOptionPane.showMessageDialog(null, "Data Added");
            } catch (SQLException e) {
            }
        }
    }//GEN-LAST:event_addDctorbtnMouseClicked

