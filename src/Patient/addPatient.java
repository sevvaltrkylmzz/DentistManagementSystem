    private void addDctorbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addDctorbtnMouseClicked
        connection = Connector.ConnectDb();
        if (connection != null) {
            String id = txtpid.getText();
            String fname = txtpfname.getText();
            String mname = txtpmname.getText();
            String lname = txtplname.getText();
            String gender = (String) pGenderField.getSelectedItem();
            String birthdate = txtpbirthdate.getText();
            String email = txtpemail.getText();
            String address = txtpaddress.getText();
            String sql = "insert into patient(PID,FName,MInit,LName,Gender,BirthDate,EMail,Address) value (?,?,?,?,?,?,?,?)";
            try {
                prp = connection.prepareStatement(sql);
                prp.setString(1, id);
                prp.setString(2, fname);
                prp.setString(3, mname);
                prp.setString(4, lname);
                prp.setString(5, gender);
                prp.setString(6, birthdate);
                prp.setString(7, email);
                prp.setString(8, address);
               
                prp.execute();
                JOptionPane.showMessageDialog(null, "Data Added");
            } catch (SQLException e) {
            }
        }
    }//GEN-LAST:event_addDctorbtnMouseClicked

