    public void loadData() {
        connection = Connector.ConnectDb();
        String sql = "select PID,FName,MInit,LName,Gender,BirthDate,Email,Address from patient";
        try {
            ps = connection.prepareStatement(sql);
            rs = ps.executeQuery();
            Object columnData[] = new Object[11];
            while (rs.next()) {
                columnData[0] = rs.getInt("PID");
                columnData[1] = rs.getString("FName");
                columnData[2] = rs.getString("MInit");
                columnData[3] = rs.getString("LName");
                columnData[4] = rs.getString("Gender");
                columnData[5] = rs.getString("BirthDate");
                columnData[6] = rs.getString("Email");
                columnData[7] = rs.getString("Address");
              
                defaultTableModel.addRow(columnData);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

